$('#contact_form').on('submit',function(e){
    e.preventDefault();
    var form_data=$(this).serialize();
    var u=$(this).attr('action');
    var m=$(this).attr('method');
    $.ajax({
        url:u,
        method:m,
        data:form_data,
        success:function(msg){
            alert(msg);
            if(msg=='success'){
                $('#errorMessage').html('<div class="alert alert-success"role="alert"><span class="glyphicon glyphicon-exclamation-sign" aria-hidden="true"></span><span class="sr-only">Error:</span>Your message has been sent</div>');
                setTimeout(clearAlerts,5000);
            }else{
                $('#errorMessage').html('<div class="alert alert-danger"role="alert"><span class="glyphicon glyphicon-exclamation-sign" aria-hidden="true"></span><span class="sr-only">Error:</span>'+msg+'</div>');
                setTimeout(clearAlerts,5000);
            }
        },
        error:function(){
            alert('Error occured!');
        }
    });
});
